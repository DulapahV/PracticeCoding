var a = 5, b = 2

// equal to operator
print(a == b) // false

// not equal to operator
print(a != b) // true

// greater than operator
print(a > b) // true

// less than operator
print(a < b) // false

// greater than or equal to operator
print(a >= b) // true

// less than or equal to operator
print(a <= b) // false